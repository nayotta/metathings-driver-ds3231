package main

import (
	"crypto/hmac"
	"crypto/sha256"
	"encoding/hex"
	"fmt"
)

var (
	message = "12345"
	key     = []byte{0x35, 0x12, 0xd0, 0x66, 0xf5, 0xed, 0x52, 0xc7, 0x79, 0x98, 0xff,
		0xaa, 0xac, 0x43, 0x22, 0x60, 0xdd, 0xff, 0x9c, 0x10, 0x99, 0x6f,
		0x41, 0x66, 0x3a, 0x60, 0x23, 0xfa, 0xf6, 0xaa, 0x3e, 0xc5}
)

func ComputeHmacSha256(message string, key []byte) string {
	h := hmac.New(sha256.New, key)
	h.Write([]byte(message))

	return hex.EncodeToString(h.Sum(nil))

}

