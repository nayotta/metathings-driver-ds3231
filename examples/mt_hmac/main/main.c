#include "stdint.h"

#include "crypto/common.h"
#include "crypto/crypto.h"
#include "crypto/sha256.h"

void app_main() {
  uint8_t key[32] = {0x35, 0x12, 0xd0, 0x66, 0xff, 0xff, 0xff, 0xff,
                     0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
                     0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
                     0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff};
  uint8_t data[5] = {0x31, 0x32, 0x33, 0x34, 0x35};
  uint8_t hmac[32] = {0x00};

  fast_hmac_sha256(key, 32, data, 5, hmac);
  for (int i = 0; i < 32; i++) {
    printf("%2x ", hmac[i]);
  }
  printf("\n");
}